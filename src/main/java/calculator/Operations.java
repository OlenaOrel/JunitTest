package calculator;

public class Operations {

    public double add(double d1, double d2){
        return d1 + d2;
    }

    public double subtract(double d1, double d2){
        return d1 - d2;
    }

    public double multiply(double d1, double d2){
        return d1 * d2;
    }

    public double divide(double d1, double d2){
        return d1 / d2;
    }

}
